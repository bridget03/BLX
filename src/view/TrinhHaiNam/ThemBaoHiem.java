    private boolean maBHExists(String maBH) {
        TreeSet<BaoHiemXeMay> list = home.getListBH();
        if(list == null) return true;
        for(BaoHiemXeMay bh: home.getListBH()) {
            if(bh.getMaBH().equals(maBH)){
                return true;
            }
        }
        return false;
    }
    
