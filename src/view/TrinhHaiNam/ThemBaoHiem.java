    private void btnThemBHActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnThemBHActionPerformed
        String maBH = txtMaBH.getText();
        String chuXe = txtChuXe.getText();
        String loaiXe = cbbLoaiXe.getSelectedItem().toString();
        String bienSo = txtBienSo.getText();
        LocalDate ngayBD = null;
        int soNam = 0;
        boolean isOK = true;
        try {
            DateTimeFormatter formatter = DateTimeFormatter.ofPattern("dd/MM/yyyy");
            ngayBD = LocalDate.parse(txtNBD.getText(), formatter);
        } catch (Exception e) {
            JOptionPane.showMessageDialog(rootPane, "Ngay nhap vao khong hop le");
            isOK = false;
        }
        
        try {           
            soNam = Integer.parseInt(txtSoNam.getText());
        } catch (Exception e) {
            JOptionPane.showMessageDialog(rootPane, "So nam phai la so nguyen");
            isOK = false;
        }
        
        
        if(maBH.length() ==0) {
            JOptionPane.showMessageDialog(rootPane, "Mã bảo hiểm không được để trống ");
            isOK = false;
        } 
        
       
        if (maBHExists(maBH)) {
            JOptionPane.showMessageDialog(rootPane, "Mã bảo hiểm đã tồn tại. Vui lòng chính xác.");
            return;
        }
        if(chuXe.length() ==0) {
            JOptionPane.showMessageDialog(rootPane, "Tên chủ xe không được để trống ");
            isOK = false;
        }      
        if(bienSo.length() ==0) {
            JOptionPane.showMessageDialog(rootPane, "Biển số xe không được để trống ");
            isOK = false;
        }            
       
        if(isOK) {
            Nguoi a = new Nguoi(chuXe);
            Xe xe1 = new Xe(bienSo, loaiXe, a);
            BaoHiemXeMay bh = new BaoHiemXeMay(xe1, ngayBD, soNam, maBH);
            home.themBH(bh);
            JOptionPane.showMessageDialog(rootPane, "Thêm bảo hiểm thành công ! ");
        }
        
    }//GEN-LAST:event_btnThemBHActionPerformed
 
